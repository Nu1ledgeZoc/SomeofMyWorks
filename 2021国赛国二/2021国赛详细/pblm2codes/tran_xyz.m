function new_xyz=tran_xyz(xyz0,tranmode)
   
    A=[-0.0380802618603818	0.985694899351720	0.164181178906956;
    0.992270249985056	0.0178800955296241	0.122800869683098;
    -0.118108615643724	-0.167588388661701	0.978756602479798];
